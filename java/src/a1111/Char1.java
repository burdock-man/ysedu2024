package a1111;

public class Char1 {
    public static void main(String[] args) {
        // char를 사용해 변수를 선언할 때 반드시 '홑따옴표' 를 사용해야한다.
        char c1 = 'a'; // 문자
        char c2 = 97; // 유니코드 97 == 소문자 a
        char c3 = '가'; // 문자
        char c4 = 44032; // 유니코드

        System.out.println("c1: " + c1);
        System.out.println("c2: " + c2);
        System.out.println("c3: " + c3);
        System.out.println("c4: " + c4);
    }
}
