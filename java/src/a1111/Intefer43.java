package a1111;

public class Intefer43 {
    public static void main(String[] args) {
        int var1 = 0b1011; // 0b binary 2진수를 뜻함. 0부터 1까지
        int var2 = 0206; // 8진수. 0부터 7까지. // 자릿수 세개씩 끊음
        int var3 = 365; // 걍 뭐 ㅇㅇ.. 10진수 ㅇㅇ..
        int var4 = 0xB3; // 16진수 // 자릿수 네개씩 끊음

        System.out.println("var1: " + var1);
        System.out.println("var2: " + var2);
        System.out.println("var3: " + var3);
        System.out.println("var4: " + var4);
    }
}
